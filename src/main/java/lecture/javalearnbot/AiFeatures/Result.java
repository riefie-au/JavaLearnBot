package lecture.javalearnbot.AiFeatures;


//Holds the answer of the generated query along with the list of chunks that provided context to that answer

import java.util.List;

public class Result {
    private final List<Hit> hits; //holds all the hits/chunks used to create the answer
    private final String answer; //holds the answer generated by the AI
    private final List<String> rewrites;

    public Result(List<Hit> hits, String answer, List<String> rewrites) {
        this.hits = hits;
        this.answer = answer;
        this.rewrites = rewrites;
    }

    public List<Hit> hits() { return hits; }
    public String answer() { return answer; }

    public String getAnswer() {
        return answer;
    }

    public List<Hit> getHits() {
        return hits;
    }

    public List<String> getRewrites() {
        return rewrites;
    }
}
